def encode(secret_message):
    cipher_dict = {
        'a': '∆', 'b': '⊗', 'c': 'ʘ', 'd': '≈', 'e': 'ξ', 'f': '☼',
        'g': '♣', 'h': '☾', 'i': '∑', 'j': '≠', 'k': '♠', 'l': '♦',
        'm': '♫', 'n': '⊕', 'o': '◉', 'p': '∞', 'q': '⌘', 'r': '☯',
        's': '★', 't': '✿', 'u': '✓', 'v': '⚑', 'w': '☂', 'x': '✦',
        'y': '☠', 'z': '✉', '!': '!', '@': '@', '#': '#', '$': '$', 
        '%': '%', '^': '^', '&': '&', '': '', '(': '(', ')': ')', ' ': '  ',
        ',': ',', '.': '.', '?': '?', ';': ';', '/': '/', '<': '<', '>': '>', 
    }
    secret_message = secret_message.lower()
    encoded = ""
    for i in secret_message:
        encoded += cipher_dict.get(i, i)
    return encoded

